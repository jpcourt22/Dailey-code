// Return the Sum of Two Numbers March/26/2024 Updated
// Create a function that takes two numbers as arguments and returns their sum.
function addition(num1, num2) {
    return num1 + num2;
}
// Examples
addition(3, 2);
// ➞ 5

addition(-3, -6);
// ➞ -9

addition(7, 3);
// ➞ 10
